#include "minishell.h"

void	ft_pwd(t_cmd *cmd)
{
	char	*pwd;

	pwd = getcwd(NULL, 1024);
	ft_putendl_fd(pwd, cmd->fd[STDOUT_FILENO]);
	free(pwd);
}